void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	count;
	int	temp;

	while (1)
	{
		i = 0;
		count = 0;
		while (i < size - 1)
		{
			if (tab[i] > tab[i + 1])
			{
				temp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = temp;
				count ++;
			}
			i ++;
		}
		if (count == 0)
			return ;
	}
}
